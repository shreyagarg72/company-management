package org.ncu.company_manage.api;

public class Project {
	private int p_id;
	private String p_name;
	private String p_reporter;
	private String assignee;
	private String p_progress;
	private String p_priority;
	private String p_level;
	private String p_comment;

	public int getP_id() {
		return p_id;
	}

	public void setP_id(int p_id) {
		this.p_id = p_id;
	}

	public String getP_name() {
		return p_name;
	}

	public void setP_name(String p_name) {
		this.p_name = p_name;
	}

	public String getP_reporter() {
		return p_reporter;
	}

	public void setP_reporter(String p_reporter) {
		this.p_reporter = p_reporter;
	}

	public String getAssignee() {
		return assignee;
	}

	public void setAssignee(String assignee) {
		this.assignee = assignee;
	}

	public String getP_progress() {
		return p_progress;
	}

	public void setP_progress(String p_progress) {
		this.p_progress = p_progress;
	}

	public String getP_priority() {
		return p_priority;
	}

	public void setP_priority(String p_priority) {
		this.p_priority = p_priority;
	}

	public String getP_level() {
		return p_level;
	}

	public void setP_level(String p_level) {
		this.p_level = p_level;
	}

	public String getP_comment() {
		return p_comment;
	}

	public void setP_comment(String p_comment) {
		this.p_comment = p_comment;
	}


	

}
